df = df[~df[USER_COL].str.lower().isin(EMPTY)]
df = df[~df[ASSISTANT_COL].str.lower().isin(EMPTY)]
df = df.dropna(subset=[USER_COL, ASSISTANT_COL])
df = df.drop_duplicates(subset=[USER_COL, ASSISTANT_COL])

